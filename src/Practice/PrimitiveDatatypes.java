// Primitive Datatypes - The datatypes which cannot be broken are primitive datatype

public class PrimitiveDatatypes {
    public static void main(String[] args) {
        int rollno = 64;                            // 4 bytes
        char letter = 'r';
        float marks = 98.67f;       // f in the end for float       4 bytes
        double largeDecimalNumbers = 468516.6568;               // 8 bytes
        long IntegerValues = 3486123845213L;        // L in the end for long        8 bytes
        boolean check = true;   // true or false

        // String is not primitive - because strings can be broken into characters
        // String name = "ritik";
    }
}
